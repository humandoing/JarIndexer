CREATE TABLE JAR_FILE (
  JAR_FILE_ID     INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  FILENAME        VARCHAR(128),
  ABSOLUTE_PATH   VARCHAR(512),
  DIRECTORY       VARCHAR(512),
  LIBRARY_NAME    VARCHAR(128),
  LIBRARY_VERSION VARCHAR(32)
);

CREATE TABLE JAR_FILE_ENTRY (
  JAR_FILE_ID     INT NOT NULL,
  ABSOLUTE_PATH   VARCHAR(512),
  CLASS_NAME      VARCHAR(128),
  FOREIGN KEY (JAR_FILE_ID) REFERENCES JAR_FILE (JAR_FILE_ID)
);

CREATE INDEX ENTRY_ABSOLUTE_PATH_INDEX ON JAR_FILE_ENTRY (ABSOLUTE_PATH);

CREATE INDEX ENTRY_CLASS_NAME_INDEX ON JAR_FILE_ENTRY (CLASS_NAME);